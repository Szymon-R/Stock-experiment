    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(click==0)
        {
            if(jTextField1.getText()==null)
            {
                JOptionPane.showMessageDialog(this, "Number you've inserted is incorrect.","Incorrect selection.",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            try
            {
                Double.parseDouble(jTextField1.getText());
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(this, "Number you've inserted is incorrect.","Incorrect selection.",JOptionPane.INFORMATION_MESSAGE);
                return;
            }


            int n = JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to continue?",
                "Confirmation",
                JOptionPane.YES_NO_OPTION);
            if(n==0)
            {
                click=1;
                jTextArea2.setVisible(true);
                jScrollPane2.setVisible(true);
                //jTextArea1.setVisible(false);
                //jScrollPane1.setVisible(false);
                jCheckBox4.setVisible(true);
                jCheckBox6.setVisible(true);

                jCheckBox9.setVisible(true);
                
                jTextField1.setEnabled(false);
                jLabel2.setVisible(true);
                jLabel5.setVisible(true);
                jLabel6.setVisible(true);
                return;
            }
        }

        if(click==1)
        {
            if(!(jCheckBox4.isSelected()||jCheckBox6.isSelected()||jCheckBox9.isSelected()))
            {
                JOptionPane.showMessageDialog(this, "You didn't choose any answer","Incorrect selection.",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            else
            {
                int n = JOptionPane.showConfirmDialog(
                    this,
                    "Are you sure you want to continue?",
                    "Confirmation",
                    JOptionPane.YES_NO_OPTION);
                if(n==0)
                {
                    ds.setLocationRelativeTo(this);
                    String text="";
                    if(jCheckBox9.isSelected())
                    text="[0-10%]";
                    if(jCheckBox6.isSelected())
                    text="[11-20%]";
                    if(jCheckBox4.isSelected())
                    text="[more than 50%]";

                    
                    System.out.println("sending "+'@'+jTextField1.getText()+'#'+'@'+text+'#');
                    while(!ClientSide1.send_data('@'+jTextField1.getText()+'#'+'@'+text+'#'));

                    final SwingWorker<Boolean, Void> worker1 =  new SwingWorker<Boolean, Void>() {

                        @Override
                        protected Boolean doInBackground() throws Exception {
                            ws.setVisible(true);
                            String lala;
                            lala=ClientSide1.read_data(1000);
                            ds.setVisible(true);
                            
                            return true;
                        }

                        // Can safely update the GUI from this method.
                        @Override
                        protected void done() {
                            ws.dispose();
                          

                            stock_screen ss=new stock_screen(ClientSide1);
                            
                            ss.addWindowListener(new WindowAdapter() 
                            {
                                public void windowClosing(WindowEvent we) 
                                {
                            int result = JOptionPane.showConfirmDialog(ss,"Are you sure you want to quit application", "Confirmation",JOptionPane.YES_NO_OPTION);
                            if(result == JOptionPane.YES_OPTION){
                            JOptionPane.showMessageDialog(ss, "Can't leave application now");
                                ss.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
                                }
                                else if(result == JOptionPane.NO_OPTION)
                                    ss.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
                                }
                            });
                            ds.dispose();
                            dispose();
                            ss.setLocationRelativeTo(null);
                            ss.setVisible(true);  
                        }
                    };
                    this.setEnabled(false);
                    ws.setLocationRelativeTo(this);
                    ws.setVisible(true);
                    worker1.execute();

                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

